export function Sidebar() {
  const pathname = usePathname();
  const { user } = useAuth();

  const filteredNavigation = navigation.filter((item) =>
    user ? item.roles.includes(user.role) : false
  );

  return (
    <div className="flex h-full w-64 flex-col bg-gray-900 text-white">
      <div className="flex h-16 items-center justify-center border-b border-gray-800 px-4">
        <h1 className="text-xl font-bold">Restaurant POS</h1>
      </div>
      <nav className="flex-1 space-y-1 px-2 py-4 overflow-y-auto scrollbar-hide">
        {filteredNavigation.map((item) => {
          // Fix active state logic to prevent multiple items being active
          let isActive = false;

          if (item.href === '/dashboard') {
            // Dashboard: exact match only
            isActive = pathname === '/dashboard';
          } else if (item.href === '/dashboard/orders/kitchen') {
            // Kitchen Display: exact match only (to prevent conflict with Orders)
            isActive = pathname === '/dashboard/orders/kitchen';
          } else if (item.href === '/dashboard/orders') {
            // Orders: match /dashboard/orders but NOT /dashboard/orders/kitchen
            isActive = pathname === '/dashboard/orders' ||
                      (pathname?.startsWith('/dashboard/orders/') && pathname !== '/dashboard/orders/kitchen');
          } else {
            // All others: startsWith check
            isActive = pathname?.startsWith(item.href) || false;
          }

          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                'flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors',
                isActive
                  ? 'bg-blue-600 text-white'
                  : 'text-gray-300 hover:bg-gray-800 hover:text-white'
              )}
            >
              {item.icon}
              {item.name}
            </Link>
          );
        })}
      </nav>
    </div>
  );
}
